package com.deveducation.chat.dto;

public class NetworkData {

    private String nameOfTheNetwork;
    private String urlOfTheNetwork;
    private String owner;


    public String getNameOfTheNetwork() {
        return nameOfTheNetwork;
    }

    public void setNameOfTheNetwork(String nameOfTheNetwork) {
        this.nameOfTheNetwork = nameOfTheNetwork;
    }

    public String getUrlOfTheNetwork() {
        return urlOfTheNetwork;
    }

    public void setUrlOfTheNetwork(String urlOfTheNetwork) {
        this.urlOfTheNetwork = urlOfTheNetwork;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }
}
